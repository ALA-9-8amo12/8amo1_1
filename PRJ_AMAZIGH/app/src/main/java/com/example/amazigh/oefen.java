package com.example.amazigh;

public class oefen {
    private String foto;
    private String ned;
    private String am;
    private String sound;

    public oefen (){
    }

    public String getFoto() {
        return foto;
    }

    public void setFoto(String foto) {
        this.foto = foto;
    }

    public String getNed() {
        return ned;
    }

    public void setNed(String ned) {
        this.ned = ned;
    }

    public String getAm() {
        return am;
    }

    public void setAm(String am) {
        this.am = am;
    }

    public String getSound() {
        return sound;
    }

    public void setSound(String sound) {
        this.sound = sound;
    }
}
